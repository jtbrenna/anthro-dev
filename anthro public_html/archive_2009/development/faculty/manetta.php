        <p>Emily Manetta, Assistant Professor of Anthropology, received her <abbr>B.A.</abbr> from Swarthmore College in Philadelphia and her <abbr>Ph.D.</abbr> from the University of California, Santa Cruz in Linguistics. Her current work focuses the syntax of South Asian languages. Her dissertation, based on linguistic field work with a Kashmiri refugee community, featured a comparative analysis of the syntax of questions in Kashmiri and Hindi-Urdu. Dr. Manetta has a strong secondary research interest in the sociolinguistics and syntax of non-standard varieties of English, in particular in her native Appalachia.</p>
        <p>Dr. Manetta will be involved in the continuing development of linguistics courses at <abbr>UVM</abbr>, teaching Introduction to Syntax, Language and Mind, and Linguistic Anthropology.</p>
