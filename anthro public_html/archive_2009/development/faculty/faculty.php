      </div><p>Cameron Wesson joined the <abbr>UVM</abbr> faculty as Associate Professor and Chair of Anthropology. He received a B.S. in architecture and a <abbr>B.A.</abbr> in anthropology from Auburn University and completed his <abbr>M.A.</abbr> and <abbr>Ph.D.</abbr> at the University of Illinois at Urbana-Champaign. He has held academic appointments at Auburn University, the University of Oklahoma, and most recently at the University of Illinois at Chicago.</p>
      <p>Dr. Wesson's archaeological research addresses the political economy of Native American societies during the contact and colonial periods in the southeastern U.S., with particular interest in the Muskogee Creek peoples. He is also concerned with examining the role of Native American peoples in the cultural dynamism of the post-contact period through the use of both archaeological data and modern Creek informants. He has published three books as well as a number of journal articles and book chapters on these topics. His most recent volume, "Households and Hegemony: Early Creek Prestige Goods, Symbolic Capital and Social Power," was released by the University of Nebraska Press in 2008.  Dr. Wesson is presently working on a new book, "Poor White Trash: An Archaeology of Sharecropping and Tenant Farming."</p>
