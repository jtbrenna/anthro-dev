      <p>After spending her childhood in Mattoon, Illinois, Deborah Blom began her college career in biomedical engineering at Northwestern University. However, discovering anthropology later in college, she ultimately received her B.S. in Anthropology from the University of Houston (1990) and her <abbr>M.A.</abbr> (1992) and <abbr>Ph.D.</abbr> (1999) in Anthropology from the University of Chicago. She joined the Anthropology faculty at the University of Vermont in 1998.</p>

<p>Blom has undertaken field work in Mesoamerica, Andean South America, and the U.S. She specializes in archaeology and biological anthropology and is interested in the study of human skeletal remains in archaeological contexts. While residing in the state of Vermont, Blom has also been active in reporting on human burials from Native and European American cemeteries that have been disturbed through construction or slated for repatriation. Building on her interests, Blom has taught several courses at <abbr>UVM</abbr> and elsewhere, including Introduction to Prehistoric Archaeology, Archaeological Theory, Laboratory Methods in Biological Anthropology, Introduction to Physical Anthropology, Origins and Evolution of Human Culture, Peoples of South America, Funerary Archaeology and Bioarchaeology, Advanced Human Evolution, Nonhuman Primate Social Behavior, Life, Death, and the Human Body, and Human Osteology.</p>

<p>Currently, Blom focuses her research in South American Andean regions of Peru and Bolivia, addressing question of health and nutrition, diversity, identity, colonization, and migration within ancient Tiwanaku society, as well as earlier and later developments. Her research has been funded by The H. John Heinz III Fund, Vanderbilt University's Discovery Grant Program, the University of Vermont's Committee on Research and Scholarship and College of Arts and Sciences Deans Fund, the Wenner Gren Foundation for Anthropological Research, the Tinker Foundation, and the William and Flora Hewlett Foundation. These pursuits have led to publications and conference papers on human sacrifice, mortuary ritual, population movement, health and diet, social complexity, trade, and human body modification as a means of expressing identity.</p>
