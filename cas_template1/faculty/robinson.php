     <p> Jess Robinson, Lecturer in Anthropology, was born and raised in Burlington, Vermont. He received his B.A. in Anthropology and in English from the University of Vermont in 1999. In 2001, he received an M.A. in literature from the University of Kent in England. After returning to Vermont, Jess was hired as a Research Supervisor by the UVM Consulting Archaeology Program; a position he continues to hold. In 2007, Jess began a Ph.D. program in Anthropology at the University at Albany-SUNY. He received his M.A. in Anthropology in 2008 and advanced to Ph.D. candidacy in the fall of 2010. He is currently finishing his Ph.D. dissertation with the support of a National Science Foundation Dissertation Improvement Grant.</p>
	 <p>Jess has participated in hundreds of archaeological investigations in Vermont and in other areas of the Northeast, and has led excavations on dozens of archaeological sites. He has also participated in archaeological field work in the central Amazon region of Brazil. Jess has previously taught undergraduate courses at Johnson State College and at UVM. </p>
	<p>Jess's Ph.D. dissertation work explores Northeastern Early Woodland mortuary ceremonialism and interregional exchange, ca. 3,000-2,000 B.P. He is interested in the nexus of religion (and its rituals) and politics, particularly during situations of cultural crisis. His other interests include ritual economy, mortuary studies, the emergence of complexity, land use and landscape identification, hunter-gatherer studies, Paleoindian studies and the adoption of agriculture among hunter-gatherer groups.</p>

